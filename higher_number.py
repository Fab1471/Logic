n1 = int(input('1st value: '))
n2 = int(input('2nd value: '))

if n1 > n2:
    print(f'1st number is higher than the 2nd number. . {n1} is higher than {n2}')
if n2 > n1:
    print(f'2nd number is higher than the 1st number. . {n2} is higher than {n1}')
else:
    print(f'Both numbers are equal. {n1} is equal to {n2}. .')
